// Helper to get related data
export const getVehicleById = (id: string) => mockVehicles.find((v) => v.id === id);
export const getCustomerById = (id: string) => mockCustomers.find((c) => c.id === id);
export const getBookingById = (id: string) => mockBookings.find((b) => b.id === id);

export const getBookingsWithDetails = (): Booking[] =>
  mockBookings.map((b) => ({
    ...b,
    customer: getCustomerById(b.customer_id),
    vehicle: getVehicleById(b.vehicle_id),
  }));

export const getPaymentsWithBookings = (): Payment[] =>
  mockPayments.map((p) => ({
    ...p,
    booking: getBookingById(p.booking_id),
  }));

// Generate booking reference
export const generateBookingRef = (): string => {
  const now = new Date();
  const dateStr = now.toISOString().slice(0, 10).replace(/-/g, "");
  const rand = Math.floor(Math.random() * 900 + 100);
  return `RR-${dateStr}-${rand}`;
};
